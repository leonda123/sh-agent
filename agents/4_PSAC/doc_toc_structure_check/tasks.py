from crewai import Task

class TOCStructureTasks:
    def parse_document_task(self, agent, pdf_path):
        return Task(
            description=f"""
            【阶段一：文档解析】
            1. 接收文件：获取位于 {pdf_path} 的 PDF 文件。
            2. 格式转换：使用文档处理工具将其转换为 Markdown 格式。
            3. 辅助产物：该工具会同时生成一个与 Markdown 同名的页码映射文件，命名规则为 `同名.pages.json`。
               例如：如果输出是 `xxx.md`，则辅助页码文件为 `xxx.pages.json`。
            4. 路径输出：确保输出转换后的 Markdown 文件绝对路径，不要对文件名中的空格或字符进行任何修改。
            
            **关键指令**：
            - 调用 `Process Document` 工具一次即可。
            - 一旦工具返回了文件路径（以 .md 结尾），**立即停止**工具调用。
            - 将该 Markdown 路径作为你的最终回答（Final Answer）。
            - **严禁**重复调用工具或进行任何额外的文本处理。
            """,
            expected_output="转换后的 Markdown 文件的精确绝对路径，不做任何修改。",
            agent=agent,
            max_retries=2
        )

    def toc_check_task_a(self, agent, parse_task):
        return Task(
            description="""
            【阶段二-A轮：目录检查】
            你是A轮检查专家，需要独立完成目录检查。
            
            ## 重要：获取文件路径
            
            **你必须从上一个任务（文档解析）的输出中提取Markdown文件的实际路径！**
            
            上一个任务输出的是 Markdown 文件的绝对路径。
            请提取该路径，不要猜测或编造路径！
            **绝对不要使用** `pages.json`、`document.md` 等猜测的相对路径！
            
            **【强制步骤】必须按顺序执行以下检查：**
            
            1. [读取数据]：
               - **首先**从上一个任务输出中提取Markdown文件的实际路径
               - 根据提取的路径推导 pages.json 路径：
                 * 如果 Markdown 路径是 `D:/outputs/document.md`
                 * 则 pages.json 路径为 `D:/outputs/document.pages.json`
               - 使用 Read File 工具读取 Markdown 和 pages.json 两个文件
               - **必须传入完整的绝对路径，不要使用相对路径！**
            
            2. [定位目录]：
               - 搜索关键词："目次"、"目录"、"Contents"、"CONTENTS"
               - 记录目录所在位置
            
            3. [提取目录项]：
               对于目录中的每一项，提取：
               - 章节编号（如 1、1.1、1.1.1）
               - 章节名称（如"目的"、"范围"、"术语定义"等）
               - 目录中标注的页码
            
            4. [验证实际页码]：
               - 根据 pages.json 中的页码映射，找到每个章节在正文中的实际位置
               - 记录实际页码
               - 比较目录页码与实际页码是否一致
            
            5. [页码连续性检查]（强制步骤，不可跳过）：
               **【关键】这是必须执行的检查步骤！**
               
               a) 从 pages.json 中提取所有 `page_label` 字段，形成完整的页码序列
               
               b) 检查页码连续性：
                  - **页码格式识别**：先判断页码格式是纯阿拉伯数字、纯罗马数字、还是罗马数字+阿拉伯数字组合
                  - **罗马数字+阿拉伯数字组合是正常情况**：如 I, II, III, IV, V, VI, VII, 8, 9, 10... 这种目录用罗马数字、正文用阿拉伯数字的情况是**正常**的，不是跳号！
                  - 起始页检查：正文页码是否从 1 开始（如果是罗马数字+阿拉伯数字组合，正文从阿拉伯数字1开始即可）
                  - 页码跳号检查：**在同一页码格式内部**检查是否连续
                    * 纯阿拉伯数字：检查 1, 2, 3, 4... 是否连续
                    * 纯罗马数字：检查 I, II, III, IV... 是否连续
                    * 罗马数字+阿拉伯数字组合：分别检查罗马数字部分和阿拉伯数字部分是否各自连续
                  - 页码重复：如出现两个页码 5
                  - **非附录页码重置（最重要）**：检查是否存在"37, 38, 1, 2..."这种模式
                    * 如果页码序列中出现"大数字 → 小数字"的跳变
                    * 检查跳变位置附近的内容是否包含"附录"关键词
                    * 如果没有"附录"标注，则判定为**页码重置错误**
               
               c) 输出页码连续性检查结果：
                  - 页码格式：[纯阿拉伯数字/纯罗马数字/罗马数字+阿拉伯数字组合]
                  - 完整页码序列（用逗号分隔）
                  - 检查结果表格
                  - 如果存在重置，详细说明重置位置和判定
            
            6. [生成检查表]：
               **【绝对禁止省略】目录中的每一项都必须完整列出！**
               按以下格式输出每一项：
               | 章节名称 | 文档中页码 | 正确页码 | 是否异常 | 异常原因 |
               | :--- | :--- | :--- | :--- | :--- |
               
               **禁止事项**：
               - 禁止使用"..."表示省略
               - 禁止使用"等"字省略
               - 禁止只列出部分目录项
               - 必须列出目录中的每一个章节
            
            **关键指令**：
            - **必须先提取上一个任务输出的实际路径**
            - 你必须使用 `Read File` 工具读取 Markdown 与 pages.json 文件
            - pages.json 路径必须根据 Markdown 路径推导，使用完整绝对路径
            - 必须执行页码连续性检查，这是强制步骤
            - 必须检查非附录页码重置
            - 正确页码应为理论页码，应为连续的正确页码
            - **罗马数字+阿拉伯数字组合是正常情况，不是跳号**
            - 目录中的每一项都必须列出，不可省略
            - 最终结果必须放在 `Final Answer:` 之后
            """,
            expected_output="完整的目录检查表和页码连续性检查结果。绝对禁止使用省略号。必须包含页码序列分析。",
            agent=agent,
            context=[parse_task],
            max_retries=3
        )

