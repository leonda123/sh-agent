from crewai import Task

class FigureTableTasks:
    def process_document_task(self, agent, pdf_path):
        return Task(
            description=f"""
            【阶段一：文档预处理】
            1. 接收文件：获取位于 {pdf_path} 的 PDF 文件。
            2. 格式转换：使用文档处理工具将其转换为机器可读的 Markdown 格式。
            3. 路径输出：确保输出转换后的文件绝对路径，不要对文件名中的空格或字符进行任何修改。
            
            **关键指令**：
            - 调用 `Process Document` 工具一次即可。
            - 一旦工具返回了文件路径（以 .md 结尾），**立即停止**工具调用。
            - 将该路径作为你的最终回答（Final Answer）。
            - **严禁**重复调用工具或进行任何额外的文本处理。
            """,
            expected_output="转换后的 Markdown 文件的精确绝对路径，不做任何修改。",
            agent=agent,
            max_retries=2
        )

    def analyze_content_task(self, agent, process_task):
        return Task(
            description="""
            【阶段二：内容提取与分析 (A轮)】
            1. [读取数据]：读取上一个任务生成的 Markdown 文件。注意：必须使用上一步输出的精确路径，不要自行添加或删除空格。
            2. [目录扫描]：优先搜索文档中的“图目录/图目次”和“表目录/表目次”章节。
            3. [目录提取]：如果找到目录，精确提取其中的每一项（图号、名称、页码）；如果未找到，明确标记状态。
            4. [全文扫描]：遍历整个文档正文，搜索所有的实际图表（如“图 1”、“Table 1”等）。
            5. [页码识别与正文提取]：记录正文中每个图表的编号、名称及所在位置。
               **重要：请严格按照提示词中的“页码识别规律”（总页数、罗马数字、| 8 |~| 9 |之间是第9页等）来准确判定和记录每个图表所在的真实页码，不要遗漏。**
            6. [原文截取]：对于正文中发现的每一个图表，**必须截取其前后约 50 字的原文作为证据**，用于后续验证。
            7. [数据输出]：生成一份结构化清单，包含：
               - 目录列表（含页码）
               - 正文列表（含位置、**原文证据片段**）

            **关键指令**：
            - 你必须使用 `Read File` 工具读取文件。
            - **严禁**在思考过程中直接输出长篇报告。
            - 最终结果必须放在 `Final Answer:` 之后。
            - 如果内容较多，请确保格式整洁，不要使用会导致解析错误的特殊字符。
            """,
            expected_output="一份包含提取的图/表目录状态、目录详细内容、正文图表列表及其**原文证据**的结构化报告。",
            agent=agent,
            context=[process_task],
            max_retries=3
        )

    def analyze_content_task_b(self, agent, process_task):
        return Task(
            description="""
            【阶段二：内容提取与分析 (B轮)】
            作为独立的复核者，请**独立**执行以下步骤（不要参考A轮结果）：
            1. [读取数据]：重新读取 Markdown 文件（路径必须精确，不要修改空格）。
            2. [独立扫描]：重新执行目录扫描和全文图表搜索。
            3. [独立提取与页码识别]：重新提取所有目录项和正文图表项，并截取证据。**务必依据“页码识别规律”（| I |，| 8 |~| 9 |是第9页等）准确定位图表所在的真实页码。**
            4. [数据输出]：生成你的独立结构化清单。

            **关键指令**：
            - 必须严格遵循 `Thought` -> `Action` -> `Observation` 的 ReAct 模式。
            - **严禁**在 Action 之前输出任何报告内容。
            - 遇到 "Failed to parse LLM response" 错误时，请直接输出 `Final Answer:` 并附上你的分析结果，**不要**再尝试进行任何思考或解释。
            - 你的最终报告必须放在 `Final Answer:` 之后。
            """,
            expected_output="一份独立的、用于比对验证的结构化报告。",
            agent=agent,
            context=[process_task],
            max_retries=3
        )

    def verify_analysis_task(self, agent, analyze_task_a, analyze_task_b, process_task):
        return Task(
            description="""
            【阶段三：双重验证与合并】
            你收到了两份独立的分析报告（A轮和B轮），以及原始文档的路径。
            1. [数据比对]：
               - 全面比对 A 轮和 B 轮报告中的所有目录项和正文图表项（包括名称、页码、编号）。
            2. [针对性验证（核心）]：
               - 如果两份报告的数据完全一致 -> 直接标记为“已验证”。
               - **如果发现不一致** -> 你必须使用 `Read File` 工具，读取原始文档（路径见上下文）中出现分歧的具体位置。
               - **仅验证分歧点**：不要重读整个文件，只读取有争议的章节或页码范围。
               - 根据工具返回的原文内容，判断哪一份报告是正确的，或者修正双方的错误。
            3. [最终合并]：
               - 生成一份**唯一的、合并后的**数据清单，仅包含经过双重验证确认的数据。
               - 对于经过原文验证修正的数据，请在备注中说明“已通过原文验证修正”。
            
            **关键指令**：
            - 当且仅当发现不一致时，才调用工具。
            - 最终输出必须以 `Final Answer:` 开头，后跟合并后的 Markdown 报告。
            - **严禁**输出 `Thought:` 或 `Action:` 作为最终答案的一部分，但**允许**在思考过程中使用它们来调用工具。
            """,
            expected_output="一份经过双重验证、消除分歧后的最终结构化数据清单。",
            agent=agent,
            context=[analyze_task_a, analyze_task_b, process_task],
            max_retries=3
        )

    def audit_content_task(self, agent, verify_task):
        return Task(
            description="""
            【阶段四：一致性审计】
            基于**经过双重验证**的数据清单，进行严格的交叉比对，并生成**Markdown 表格**形式的证据：
            1. [存在性核查]：确认图目录和表目录是否真实存在。
            2. [构建比对表-表]：
               创建一个 Markdown 表格，列分别为：| 目录中的表名 | 正文中的表名 | 是否一致 | 差异描述 | 原文证据 |
               - 逐行比对，填入每一项。
               - 如果目录有但正文无，正文栏填“缺失”。
               - 如果正文有但目录无，目录栏填“缺失”。
            3. [构建比对表-图]：
               创建一个 Markdown 表格，列分别为：| 目录中的图名 | 正文中的图名 | 是否一致 | 差异描述 | 原文证据 |
               - 同样逐行比对。
            4. [统计差异]：基于表格统计不一致的数量和类型。
            5. [初步结论]：基于比对结果，给出合格/不合格的初步判定。
            """,
            expected_output="一份包含详细**比对表格**和统计数据的审计报告。",
            agent=agent,
            context=[verify_task],
            max_retries=2
        )

    def review_and_report_task(self, agent, audit_task):
        return Task(
            description="""
            【阶段五：最终复审与报告生成】
            作为最终把关人，生成一份**专业级**的审计报告。报告必须严格遵循以下 Markdown 格式：
            
            # 文档图表一致性审计报告


            ## 1. 审计综述
            | 项目 | 详情 |
            | :--- | :--- |
            | 文档名称 | [文件名] |
            | 审计日期 | [当前日期] |
            | 审计结果 | **[通过/不通过]** |
            | 关键发现 | [一句话概括核心问题] |

            ## 2. 目录存在性核查
            | 检查项 | 状态 | 证据说明 |
            | :--- | :--- | :--- |
            | 表目录 | [存在/缺失] | [例如：第3页发现“表目录”章节] |
            | 图目录 | [存在/缺失] | [例如：全文搜索未发现“图目录”关键词] |

            ## 3. 表录与正文一致性审计
            **总体结论**：[一致/不一致/部分一致]

            **详细比对表**：
            [此处插入审计员生成的“表比对表格”，确保包含“原文证据”列]

            ## 4. 图录与正文一致性审计
            **总体结论**：[一致/不一致/部分一致]

            **详细比对表**：
            [此处插入审计员生成的“图比对表格”，确保包含“原文证据”列]

            ## 5. 差异汇总与修订建议
            > 以下列出所有阻碍文档通过审查的关键问题：

            ###  审查差异点 
            [差异点1]
               - **差异描述**：[例如：表1名称不匹配]
               - **原文证据**：[引用正文原文]
               - **修订建议**：[具体修改意见]


            ## 6. 最终结语
            [对文档质量的最终专业评估]
            """,
            expected_output="一份包含**专业表格**、**原文引用**和**详细比对数据**的 Markdown 最终审计报告。",
            agent=agent,
            context=[audit_task],
            max_retries=2
        )
